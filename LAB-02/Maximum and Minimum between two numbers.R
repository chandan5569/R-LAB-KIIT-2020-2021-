
num1 <- as.integer(readline("Enter the first number \n"))

num2 <- as.integer(readline("Enter the second number \n"))

c=max(num1,num2)      #using max() in built function to find maximum between two numbers 

cat('\n Maximum number is ',c)

d=min(num1,num2)      #using min() in built function to find minimum between two numbers 

cat('\n Minimum number is ',d)
